package topics

/**
 * Kotlin строго типилизированный язык
 * Kotlin может предугадывать тип переменной
 * val - константа
 * var - переменная
 */

fun main() {

    val test1: String = "Hello"

    var test2: String = "Good morning"
    test2 = "Good night"

    /**
     * равнозначная запись с
     */

    val test3 = "Hello"

    var test4 = "Good morning"

}